#include "../includes/ft_printf.h"

int		ft_check_type(char c)
{
	return (c == 's' || c == 'd' || c == 'c' || c == 'p' || c == '%' || 
			c == 'i' || c == 'u' || c == 'x' || c == 'X');
}

int		ft_check_flags(char c)
{
	return (c == '0' || c == '-' || c == '*' || c == '.');
}

